package com.oracle.entity;

public class show {

	private String ss;
	private Integer dd;
	@Override
	public String toString() {
		return "show [ss=" + ss + ", dd=" + dd + "]";
	}
	public String getSs() {
		return ss;
	}
	public void setSs(String ss) {
		this.ss = ss;
	}
	public Integer getDd() {
		return dd;
	}
	public void setDd(Integer dd) {
		this.dd = dd;
	}
	
}
